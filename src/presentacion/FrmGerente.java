    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre, profesion;
        nombre = txtNombre.getText();
        profesion = txtProfesion.getText();
        gerente = new Gerente(nombre, profesion);
        JOptionPane.showMessageDialog(this, "Los datos han sido registrados",
                "Registro", JOptionPane.INFORMATION_MESSAGE);
        JOptionPane.showMessageDialog(this, gerente.toString(),
                "Datos del gerente", 1);
        limpiarCajas();
        activar(false);
        btnSalir.requestFocus();        
    }//GEN-LAST:event_btnGuardarActionPerformed

